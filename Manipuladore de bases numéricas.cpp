//Manipuladore de bases numéricas
#include <iostream>
#include <cstdlib>

using namespace std;

int main()
{
    int n=65;
    cout << '\n' << "Hexadecimal: " << hex << n;
    cout << '\n' << "Octal:       " << oct << n;
    cout << '\n' << "Decimal:     " << dec << n;
    cout << endl;
    system("PAUSE");
    return 0;
}
